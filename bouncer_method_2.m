
%----------------------------% VARIABLES %------------------------------%
theta = 41.5;
l = 1.005;
g = 9.81;
h2 = 0.825;
e = 0.6757;
friction = 0.2;

%-----------------------------% STAGE 1 %-------------------------------%
v1 = sqrt( 2*((g*l*sind(theta)) - (l*friction*g*cosd(theta))) );
v1x = v1 * cosd(theta);
v1y = v1 * sind(theta);


%-----------------------------% STAGE 2 %-------------------------------%
v2y = sqrt((v1y^2) + (2*g*h2));
v2x = v1x;

t2 = (v2y - v1y)/g;
d2 = v2x * t2;

%-----------------------------% STAGE 3 %-------------------------------%
v3x = v2x;
v3y = e*(-v2y);

%-----------------------------% STAGE 4 %-------------------------------%
t4 = (-v3y)/g;
d3 = (2*t4)*(v3x);

%----------------------------% FINDING Dt %-----------------------------%
dt = d3 + d2
